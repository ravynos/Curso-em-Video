"""Escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milímetros"""

metros = float(input("Quantos metros você deseja converter? "))
centimetros = metros * 100
milimetros = metros * 1000

print(f"{metros} metros equivale a {centimetros} centimetros é {milimetros} milimetros")